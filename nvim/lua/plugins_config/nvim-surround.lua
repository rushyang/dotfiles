--[====[nvim-surround has 3 main operations:
 Add surround - ys (Stands for [Y]ou [S]urround)
 Change - cs - [C]hange [S]urround
 Deletes - ds - [D]elete [S]urround

 : Additional operators:
 iw: [i]nner [w]ord
 a) - around )
l - around character
t; - till ; - but before it
End bracket doesn't add space, start bracket adds it

Visual mode: Select area: "S" and then surround character
--]====]
